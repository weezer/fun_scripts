class Solution(object):
    def numIslands(self, grid):
        """
        :type grid: List[List[str]]
        :rtype: int
        """
        self.len_row = len(grid)
        if self.len_row == 0:
            return 0
        self.len_column = len(grid[0])
        count = 0
        for i in range(self.len_row):
            for j in range(self.len_column):
                if grid[i][j] == "1":
                    self.dfs(i, j, grid, count)
                    count += 1
        return count

    def dfs(self, row, column, grid, count):
        if row < 0 or row >= self.len_row or column < 0 or column >= self.len_column or grid[row][column] != "1":
            return
        grid[row][column] = count
        self.dfs(row - 1, column, grid, count)
        self.dfs(row + 1, column, grid, count)
        self.dfs(row, column - 1, grid, count)
        self.dfs(row, column + 1, grid, count)

